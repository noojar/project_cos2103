     public void INSTWL(DNode LOCA,DNode LOCB, DNode NEW){      
       LOCA.FORW = NEW; NEW.FORW = LOCB;
       LOCB.BACK = NEW; NEW.BACK = LOCA;       
    }
     
  public  void DELTWL(DNode LOC){      
       LOC.BACK.FORW = LOC.FORW;
       LOC.FORW.BACK = LOC.BACK;
    }    
