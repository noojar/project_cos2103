        int data = Integer.parseInt(jTextField2.getText());
        if(START==null){  //โนดแรก
            INSTFIRST(data); //เพิ่มโนด
            LAST=START;
        } 
        else      //โนดที่สอง
            if(START.FORW==null){
                INSTFIRST(data);
                START.FORW.BACK = START;
                LAST = START.FORW;
            }
            else  //โนดอื่นๆ 
             {
               DNode temp = new DNode();
               temp.INFOR = data;
           //    LAST = temp;
               INSTWL(START, START.FORW, temp); 
        }  
        showDatainList(); //แสดงข้อมูลในลิสต์
        int firstdata = (int)START.INFOR;
        String f = String.valueOf(firstdata);
        jTextField1.setText(f);
        cur=START;
