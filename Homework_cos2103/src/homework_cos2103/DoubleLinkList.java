    public void INSTWL(DNode LOCP, DNode LOC, DNode NEW) {
        LOCP.FORW = NEW;
        NEW.FORW = LOC;
        LOC.BACK = NEW;
        NEW.BACK = LOCP;
    }

    public void DELTWL(DNode LOC) {
        LOC.BACK.FORW = LOC.FORW;
        LOC.FORW.BACK = LOC.BACK;
    }

