    public void INSTFIRST(String id, String fname, String lname, int salary) {
        //Algorrithm 5.4 SEARCH  if AVAIL = NULL step. 1

        DNode NEW = new DNode();   //step. 2
        NEW.id = id;
        NEW.fname = fname;
        NEW.lname = lname;
        NEW.salary = salary;
        NEW.FORW = START;
        NEW.BACK = null;                //เพิ่มเติมเข้ามา
            
        if (START != null) {            //เพิ่มเติมเข้ามา
            START.BACK = NEW;           //เพิ่มเติมเข้ามา
            
        }
        START = NEW;

    }

    public void InsertAfter(DNode START, String id, String fname, String lname, int salary) {
        if (START == null) {
            System.out.println("The given previous node cannot be NULL ");
            return;
        }
        DNode NEW = new DNode();
        NEW.id = id;
        NEW.fname = fname;
        NEW.lname = lname;
        NEW.salary = salary;

        //new_node.next = prev_Node.next;
        NEW.FORW = START.FORW;

        /* Make the next of prev_node as new_node */
        //prev_Node.next = new_node;
        START.FORW = NEW;

        /* 6. Make prev_node as previous of new_node */
        //new_node.prev = prev_Node;
        NEW.BACK = START;

        /*Change previous of new_node's next node */
//        if (new_node.next != null) {
//            new_node.next.prev = new_node;
//        }
        if (NEW.FORW != null) {
            NEW.FORW.BACK = NEW;

        }
    }

    public boolean FINDB(String data) {

        DNode PTR = null;
        DNode SAVE = null;
        PTR = START;
        if (START == null) {
            LOC = null;
            LOCP = null;
            return true;
        }
        if (START.id.equals(data)) {
            LOC = START;
            LOCP = null;
            return true;
        }

        SAVE = START;
        PTR = START.FORW;
        while (PTR != null) {
            if (PTR.id.equals(data)) {

                LOC = PTR;
                LOCP = SAVE;
                return true;
            }
            SAVE = PTR;
            PTR = PTR.FORW;
        }
        LOC = null;
        //Show Result;
        if (LOC == null) {
            System.out.println("Not found.");
            //jTextArea1.setText("Not found.");
            return false;
        } else {
            System.out.println("Found.");
            //jTextArea1.setText("ค้นพบ");
//            jTextArea1.setText(jTextArea1.getText() + "รหัสพนักงาน : " + PTR.id + "\n"
//                    + "ชื่อ : " + PTR.fname + "\n"
//                    + "นามสกุล : " + PTR.lname + "\n"
//                    + "เงินเดือน : " + PTR.salary + "\n"
//                    + "--------------------------------" + "\n");
            return true;
        }
    }

